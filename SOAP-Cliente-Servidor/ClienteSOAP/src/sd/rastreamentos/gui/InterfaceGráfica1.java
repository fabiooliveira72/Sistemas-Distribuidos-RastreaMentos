    private void btn_buscar_listarPosicaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscar_listarPosicaoActionPerformed
        // TODO add your handling code here:

        if(!texto_cod_listarPosicao.getText().isEmpty()){
            Integer codigo = Integer.parseInt(texto_cod_listarPosicao.getText());
            List<Posicao> ls = null;

            if(!texto_dia_listarPosicao.getText().isEmpty())
            {
                GregorianCalendar cal = new GregorianCalendar();
                cal.set(Calendar.YEAR, Integer.parseInt(texto_ano_listarPosicao.getText()));
                cal.set(Calendar.MONTH, Integer.parseInt(texto_mes_listarPosicao.getText())-1);
                cal.set(Calendar.DAY_OF_MONTH, Integer.parseInt(texto_dia_listarPosicao.getText()));
                cal.set(Calendar.HOUR_OF_DAY, Integer.parseInt(texto_hora_listarPosicao.getText()));
                cal.set(Calendar.MINUTE, Integer.parseInt(texto_min_listarPosicao.getText()));
                cal.set(Calendar.SECOND, Integer.parseInt(texto_seg_listarPosicao.getText()));
                try{
                    XMLGregorianCalendar xmlDate = DatatypeFactory.newInstance().newXMLGregorianCalendar(cal);
                    ls = ListaPosicaoVeiculoID.ListaPosicao(codigo, xmlDate);
                } catch (DatatypeConfigurationException ex) {
                    Logger.getLogger(InterfaceGráfica1.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
            else{
                ls = ListaPosicaoVeiculoID.ListaPosicao(codigo, null);
            }

            attTabela(dtm_listarPosicao);
            for (Posicao p : ls) {
                inserirItemTabela2(p, dtm_listarPosicao);
            }

        }

    }//GEN-LAST:event_btn_buscar_listarPosicaoActionPerformed

