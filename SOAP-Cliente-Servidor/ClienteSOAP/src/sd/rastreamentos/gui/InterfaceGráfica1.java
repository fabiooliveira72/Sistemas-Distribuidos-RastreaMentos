    private void btn_excluir_delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluir_delActionPerformed
        // TODO add your handling code here:
        if (texto_cod_del.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Campo cógigo é obrigatório!");
            return;
        }

        veiculo = ConsultaVeiculoID.ConsultaVeiculo(Integer.parseInt(texto_cod_del.getText()));

        if (veiculo == null) {
            JOptionPane.showMessageDialog(this, "O veiculo informado não existe");
            texto_cod_del.setText("");
        } else {
            if (DeleteVeiculoID.DeletaVeiculo(Integer.parseInt(texto_cod_del.getText()))) {
                JOptionPane.showMessageDialog(this, "Veiculo excluído com sucesso");
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir veiculo");
            }
            texto_cod_del.setText("");
        }
    }//GEN-LAST:event_btn_excluir_delActionPerformed

    private void btn_buscar_listarTipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscar_listarTipoActionPerformed
        // TODO add your handling code here:
        Integer tipo = verificarDigitos(texto_tipo_listarTipo.getText());
        if(tipo == null){
            texto_tipo_listarTipo.setText("");
            return;
        }
        attTabela(dtm_listarTipo);
        List<Veiculo> ls = ListaTipoID.ListaTipo(tipo);
        
        try {
            for (Veiculo v : ls) {
                inserirItemTabela(v, dtm_listarTipo);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "O código não foi encontrado");
        }
    }//GEN-LAST:event_btn_buscar_listarTipoActionPerformed

